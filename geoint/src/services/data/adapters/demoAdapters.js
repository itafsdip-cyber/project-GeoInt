import { enrichFeedWithOsint, normalizeSeverity, normalizeVerification } from "../normalizedEventModel";

const DEMO_CLOCK_UTC = new Date("2026-03-10T04:30:00.000Z");

const withDemoTimestamp = (index, spacingHours = 1.5, startOffsetHours = 0) =>
  new Date(DEMO_CLOCK_UTC.getTime() - ((startOffsetHours + index * spacingHours) * 3600000)).toISOString();

export const buildDemoFeed = ({ alerts, events, timeline, trajectories, sources }) => enrichFeedWithOsint({
  alerts: alerts.map((alert, index) => ({
    id: `alert-${alert.id || index}`,
    type: "alert",
    category: "incident",
    title: alert.title,
    source: alert.srcKey || alert.source || "Unknown",
    timestamp: withDemoTimestamp(index, 1.2, 0.2),
    latitude: alert.latitude ?? null,
    longitude: alert.longitude ?? null,
    severity: normalizeSeverity(alert.sev),
    verificationStatus: normalizeVerification(alert.verified),
    region: alert.loc || "Unknown",
    metadata: { ...alert, feedType: "alerts" },
  })),
  events: events.map((event, index) => ({
    id: `event-${event.id || index}`,
    type: event.type?.toLowerCase() || "event",
    category: "strike",
    title: event.title,
    source: event.sources?.[0]?.key || "Unknown",
    timestamp: withDemoTimestamp(index, 2.1, 0.1),
    latitude: event.lat ?? null,
    longitude: event.lng ?? null,
    severity: normalizeSeverity(event.sev),
    verificationStatus: normalizeVerification(Boolean(event.verified)),
    region: event.loc || "Unknown",
    metadata: { ...event, feedType: "events" },
  })),
  timeline: timeline.map((item, index) => ({
    id: `timeline-${index}`,
    type: item.type?.toLowerCase() || "timeline",
    category: "timeline",
    title: item.event,
    source: item.src || "Unknown",
    timestamp: withDemoTimestamp(index, 6, 1),
    latitude: null,
    longitude: null,
    severity: "medium",
    verificationStatus: "verified",
    region: "Regional",
    metadata: { ...item, feedType: "timeline" },
  })),
  sources: Object.entries(sources).map(([key, source], index) => ({
    id: `source-${key}-${index}`,
    type: "source",
    category: "source",
    title: source.name,
    source: key,
    timestamp: withDemoTimestamp(index, 10, 2),
    latitude: null,
    longitude: null,
    severity: "low",
    verificationStatus: "verified",
    region: "Global",
    metadata: { ...source, feedType: "sources" },
  })),
  trajectories: trajectories.map((trajectory, index) => ({
    id: `trajectory-${trajectory.id || index}`,
    type: trajectory.type?.toLowerCase() || "trajectory",
    category: "trajectory",
    title: trajectory.label,
    source: trajectory.srcKey || "Unknown",
    timestamp: withDemoTimestamp(index, 0.8, 0.4),
    latitude: trajectory.from?.[0] ?? null,
    longitude: trajectory.from?.[1] ?? null,
    severity: trajectory.intercepted ? "medium" : "high",
    verificationStatus: "verified",
    region: `${trajectory.fromLabel || "Unknown"} → ${trajectory.toLabel || "Unknown"}`,
    metadata: { ...trajectory, feedType: "trajectories" },
  })),
  sourceStatuses: {
    gdelt: { provider: "gdelt", state: "unavailable", reason: "Demo mode" },
    reddit: { provider: "reddit", state: "unavailable", reason: "Demo mode" },
    x: { provider: "x", state: "unavailable", reason: "Demo mode" },
    rss: { provider: "rss", state: "unavailable", reason: "Demo mode" },
  },
  generatedAt: DEMO_CLOCK_UTC.toISOString(),
});

export const buildEmptyLiveFeed = (reason = "No live data") => enrichFeedWithOsint({
  alerts: [],
  events: [],
  timeline: [],
  trajectories: [],
  sources: [],
  sourceStatuses: {},
  generatedAt: new Date().toISOString(),
  metadata: { reason },
});
